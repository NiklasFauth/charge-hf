void HAL_HRTIM_RepetitionEventCallback(HRTIM_HandleTypeDef * hhrtim, uint32_t TimerIdx) {
  if (TimerIdx == HRTIM_TIMERINDEX_TIMER_D) {
    //sepic.Icycle = (adc2_regular_buf[0] * VDDA)/0xFFF;
    //sepic.Icycle = sepic.Icycle / (0.01 * 64.0); // 10m sense, OPAMP2 64 Gain

    sepic.Vout = (adc2_regular_buf[0] * VDDA)/0xFFF;
    sepic.Vout = (sepic.Vout / (5100.0 / (100000.0 + 5100.0))) * 1.207; // 100k by 5k1 divider

    sepic.Iin = (adc2_regular_buf[1] * VDDA)/0xFFF;
    sepic.Iin = sepic.Iin / (0.01 * (2000.0 / 33.0)); // Si8540, 10m sense, 2k Rout, 33R Rg

    sepic.Vin = (adc2_regular_buf[2] * VDDA)/0xFFF;
    sepic.Vin = sepic.Vin / (5100.0 / (5100.0 + 5100.0)); // 5k1 by 5k1 divider

    float PID_I = 2.2;

    if (sepic.Iin < sepic.Iset) {
      sepic.error += (sepic.Vset - sepic.Vout) * PID_I;
    } else {
      sepic.error += (sepic.Iset - sepic.Iin) * 1000.0;
    }

    if (sepic.error < 0) {
      sepic.error = 0;
    } else if (sepic.error > 16000) {
      sepic.error = 16000;
    }
    uint32_t compare = sepic.error + 100;
    //if (sepic.Iin > sepic.Iset) {
    //  LL_HRTIM_TIM_SetCompare1(HRTIM1, LL_HRTIM_TIMER_D, 100);
    //} else
    if (compare < (SEPIC_PERIOD * 0.9f) && compare > 100) {
      LL_HRTIM_TIM_SetCompare1(HRTIM1, LL_HRTIM_TIMER_D, compare);
    }
  }
}

